import {
  mergeRebateDeals,
  parseRebateDeals,
  type AllDeals,
} from "parsing/parseRebateDeals";
import { parseArguments } from "utils/parseArguments";
import type { Address } from "viem";
import type { Command } from "@commander-js/extra-typings";
import type { RebateDeals } from "core/types";
import {
  parseOffchainReferrals,
  type OffChainReferral,
} from "parsing/parseOffchainReferrals";
import { isWildCard } from "utils/various";

export function setUserFeeCommand(command: Command) {
  command
    .command("user-fee")
    .description(
      "Calculate and generate fee reports for a specified vault, including referral rewards and rebates for all users. The output is a csv with the following columns: chainId, vault, wallet, balance, fees, pricePerShare, cashback.\
 This command require precise block input. The fromBlock and the toBlock must correspond to totalAssets updates blockNumber."
    )
    .argument("chainId:VaultAddress")
    .requiredOption(
      "-f, --from-block <number>",
      "Starting block number for fee computation (exclusive). Use 'find-blocks' command to find the appropriate block number"
    )
    .requiredOption(
      "-t, --to-block <number>",
      "Ending block number for fee computation (inclusive). Use 'find-blocks' command to find the appropriate block number"
    )
    .option("-r, --readable", "Format the output in a human-readable format")
    .option(
      "-o, --output",
      "Will save the result in output/user-fee in a file with following format: <chainId>-<vaultAddress>-<from-block>-<to-block>.csv"
    )
    .option(
      "--silent",
      "This will prevent the printing of the output on stdout",
      false
    )
    .option(
      "-d, --deals <string>",
      "Path to the configuration file containing OTC (Over-The-Counter) deals on the fee rebate. The amount of % is express in 10^2. For example, 8% is express 800 in the csv file."
    )
    .option(
      "--referrals <string>",
      "Path to the configuration file containing OTC (Over-The-Counter) referrals on the fee rewards. The amount of % is express in 10^2. For example, 8% is express 800 in the csv file."
    )
    .option(
      "--fee-rebate-rate <number>",
      "Bips of fees to be refunded to the referred user (e.g., 500 means 5% of fees are refunded, 5 means 0.05% of fees are refunded)",
      "500"
    )
    .option(
      "--fee-reward-rate <number>",
      "Bips of fees to be distributed to the referrer (e.g., 1500 means referrer receives 15% of fees generated by the referred user). When a user refer another user, he become his lifetime referrer. We do not overwrite referals.",
      "1500"
    )
    .addHelpText(
      "after",
      `
Example:
  $ bun user-fee 1:0x07ed467acd4ffd13023046968b0859781cb90d9b -f 1000000 -l 2000000 -r -o -d dealsExample.csv
    `
    )
    .action(async (args, options) => {
      const vault = parseArguments(args);

      let deals: AllDeals = {};

      // prepare deals object
      if (options.deals) deals = await parseRebateDeals(options.deals);
      const rebateDeals: RebateDeals = mergeRebateDeals(
        deals[0]?.["0x0"] || {}, // those are wildcard deals
        deals[vault.chainId]?.[vault.address.toLowerCase() as Address] || {} // those are vault level deals
      );

      let offChainReferrals: OffChainReferral[] | undefined;
      if (options.referrals) {
        offChainReferrals = await parseOffchainReferrals(options.referrals);
        offChainReferrals = offChainReferrals.filter(
          (referral) =>
            isWildCard(referral.chainId, referral.vault) ||
            (referral.chainId === vault.chainId &&
              referral.vault.toLowerCase() == vault.address.toLowerCase())
        );
      }

      const result = await processVault({
        rebateDeals,
        offChainReferrals,
        readable: options!.readable!,
        defaultReferralRate: Number(options!.feeRebateRate!),
        defaultRebateRate: Number(options!.feeRewardRate!),
        vault,
        fromBlock: BigInt(options.fromBlock),
        toBlock: BigInt(options.toBlock),
        strictBlockNumberMatching: true,
      });

      const csv = convertToCSV(result);
      if (options.silent == false) {
        console.log(csv);
      }
      if (options.output) {
        try {
          const file = Bun.file(
            `./output/user-fee/${vault.chainId}-${vault.address}-${options.fromBlock}-${options.toBlock}.csv`
          );
          await file.write(csv);
          console.log(`CSV report written to: ${file.name}`);
        } catch (error: any) {
          console.error("Error writing CSV file:", error.message);
          console.log("CSV content:");
          console.log(csv);
        }
      }
    });
}

function convertToCSV(vault: {
  chainId: number;
  address: string;
  pricePerShare: number;
  data: Record<
    string,
    {
      balance: number;
      fees: number;
      cashback: number;
    }
  >;
}) {
  const csvRows = [
    `chainId,vault,wallet,balance,fees,pricePerShare,cashback$`, // CSV header
    ...Object.entries(vault.data).map(
      ([address, { balance, fees, cashback }]) => {
        if (balance === 0) return "";
        let str = `${vault.chainId},${vault.address},${address},${balance},${fees}`;
        str += `,${vault.pricePerShare},${cashback}`;
        return str;
      }
    ),
  ];
  return csvRows.filter((row) => row !== "").join("\n");
}

